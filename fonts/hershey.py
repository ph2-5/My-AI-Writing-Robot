class _TrueTypeRenderer:
    _instance: Optional["_TrueTypeRenderer"] = None
    _initialized: bool = False
    _font: Optional[object] = None
    _available: Optional[bool] = None

    @classmethod
    def get_instance(cls) -> Optional["_TrueTypeRenderer"]:
        if cls._instance is None:
            cls._instance = cls()
        return cls._instance if cls._instance._ensure_loaded() else None

    def _ensure_loaded(self) -> bool:
        if self._available is not None:
            return self._available
        try:
            from fontTools.ttLib import TTFont
            self._available = False
            for font_path in _WINDOWS_FONT_CANDIDATES:
                if os.path.isfile(font_path):
                    try:
                        self._font = TTFont(font_path, fontNumber=0)
                        self._available = True
                        break
                    except Exception:
                        continue
        except ImportError:
            self._available = False
        return self._available

    def get_glyph_points(self, char: str, x: float, y: float, font_size: float) -> Optional[List[Point]]:
        if self._font is None:
            return None
        try:
            from fontTools.pens.recordingPen import RecordingPen
            glyph_set = self._font.getGlyphSet()
            cmap = self._font.getBestCmap()
            glyph_name = cmap.get(ord(char))
            if glyph_name is None:
                return None
            pen = RecordingPen()
            glyph_set[glyph_name].draw(pen)
            units_per_em = self._font['head'].unitsPerEm
            scale = font_size / units_per_em
            points: List[Point] = []
            subpath_start = None
            for op, args in pen.value:
                if op == 'moveTo':
                    if subpath_start is not None and points:
                        points.append(Point(subpath_start.x, subpath_start.y))
                        points.append(Point(float('nan'), float('nan')))
                    px = x + args[0][0] * scale
                    py = y - args[0][1] * scale
                    pt = Point(px, py)
                    points.append(pt)
                    subpath_start = pt
                elif op == 'lineTo':
                    px = x + args[0][0] * scale
                    py = y - args[0][1] * scale
                    points.append(Point(px, py))
                elif op == 'qCurveTo':
                    for pt in args:
                        if pt is not None:
                            px = x + pt[0] * scale
                            py = y - pt[1] * scale
                            points.append(Point(px, py))
                elif op == 'curveTo':
                    for pt in args:
                        px = x + pt[0] * scale
                        py = y - pt[1] * scale
                        points.append(Point(px, py))
                elif op == 'closePath':
                    if subpath_start is not None:
                        points.append(Point(subpath_start.x, subpath_start.y))
                    subpath_start = None
            if subpath_start is not None and points:
                points.append(Point(subpath_start.x, subpath_start.y))
            return points if points else None
        except Exception:
            return None

    def get_glyph_width(self, char: str, font_size: float) -> Optional[float]:
        if self._font is None:
            return None
        try:
            cmap = self._font.getBestCmap()
            glyph_name = cmap.get(ord(char))
            if glyph_name is None:
                return None
            hmtx = self._font['hmtx']
            advance_width = hmtx[glyph_name][0]
            units_per_em = self._font['head'].unitsPerEm
            scale = font_size / units_per_em
            return advance_width * scale
        except Exception:
            return None


def _is_cjk(char: str) -> bool:
    cp = ord(char)
    return 0x4E00 <= cp <= 0x9FFF


def get_char_strokes(char: str, x: float, y: float, font_size: float) -> List[Point]:
    scale = font_size / 9.0

    if char in _HERSHEY_SIMPLEX:
        strokes_data = _HERSHEY_SIMPLEX[char]
        points: List[Point] = []
        for seg in strokes_data:
            px = x + seg[0] * scale
            py = y - seg[1] * scale
            points.append(Point(px, py))
            px2 = x + seg[2] * scale
            py2 = y - seg[3] * scale
            points.append(Point(px2, py2))
        return points

    if _is_cjk(char):
        renderer = _TrueTypeRenderer.get_instance()
        if renderer is not None:
            glyph_points = renderer.get_glyph_points(char, x, y, font_size)
            if glyph_points is not None:
                return glyph_points

        if char in _CJK_STROKES:
            strokes_data = _CJK_STROKES[char]
            points: List[Point] = []
            for seg in strokes_data:
                px = x + seg[0] * scale
                py = y - seg[1] * scale
                points.append(Point(px, py))
                px2 = x + seg[2] * scale
                py2 = y - seg[3] * scale
                points.append(Point(px2, py2))
            return points

        return _draw_unknown_char(x, y, font_size)

    return _draw_unknown_char(x, y, font_size)


def _draw_unknown_char(x: float, y: float, font_size: float) -> List[Point]:
    scale = font_size / 9.0
    s = 3 * scale
    cx = x + 4 * scale
    cy = y - 4 * scale
    return [
        Point(cx - s, cy - s),
        Point(cx + s, cy - s),
        Point(cx + s, cy + s),
        Point(cx - s, cy + s),
        Point(cx - s, cy - s),
    ]


def get_char_width(char: str, font_size: float) -> float:
    scale = font_size / 9.0
    if _is_cjk(char):
        renderer = _TrueTypeRenderer.get_instance()
        if renderer is not None:
            glyph_w = renderer.get_glyph_width(char, font_size)
            if glyph_w is not None:
                return glyph_w
        return _CJK_WIDTH * scale * 1.2
    return _CHAR_WIDTH * scale * 1.2
